    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        try{
            JSONObject selected = rList.get(jList1.getSelectedIndex());
            String text = "Title: " + selected.getString(YouTubeVideo.TITLE_KEY) + "\n"
            +"URL: https://www.youtube.com/watch?v=" + selected.getString(YouTubeVideo.ID_KEY) + "\n"
            + "Duration: " + selected.getString(YouTubeVideo.DURATION_KEY) + " seconds \n"
            + "Likes: " + selected.getString(YouTubeVideo.LIKE_CT_KEY) + "\n"
            + "Dislikes: " + selected.getString(YouTubeVideo.DISLIKE_CT_KEY) + "\n"
            + "Favorites: " + selected.getString(YouTubeVideo.FAVORITED_CT_KEY) + "\n"
            + "Description: " + selected.getString(YouTubeVideo.DESC_KEY) ;
            jTextPane1.setText(text);
            curVidLink = "https://www.youtube.com/watch?v=" + selected.getString(YouTubeVideo.ID_KEY);
            jLabel5.setText(selected.getString(YouTubeVideo.TITLE_KEY));
            double likes = Double.parseDouble(selected.getString(YouTubeVideo.LIKE_CT_KEY)), dislikes = Double.parseDouble(selected.getString(YouTubeVideo.DISLIKE_CT_KEY));
            jLabel8.setText(Math.round((likes/(likes+dislikes)) * 100) + "%");
            jLabel4.setText(selected.getString(YouTubeVideo.DURATION_KEY) + "s");
        } catch (Exception e ){
            System.out.println("Error Occured During Search: " + e.getMessage());
        }
    }//GEN-LAST:event_jList1ValueChanged

