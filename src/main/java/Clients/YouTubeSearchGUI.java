    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{
            JSONObject results = new JSONObject(search.search(jTextField1.getText()));
            JSONArray rArray = results.getJSONArray("videos");
            accepted = new JSONArray();
            rList = new ArrayList();
            DefaultListModel list = new DefaultListModel();
            for(int x = 0; x < rArray.length(); x++){
                rList.add(rArray.getJSONObject(x));
                list.addElement(rArray.getJSONObject(x).get(YouTubeVideo.TITLE_KEY));
            }
            jList1.setModel(list);
        } catch (Exception e){
            System.out.println("Error Occured During Search: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

