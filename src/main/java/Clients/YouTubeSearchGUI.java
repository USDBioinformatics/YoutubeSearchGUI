    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileNameExtensionFilter(".txt File", "txt"));
        chooser.setDialogType(JFileChooser.SAVE_DIALOG);
        try {
            int rVal = chooser.showSaveDialog(this);
            if (rVal == JFileChooser.APPROVE_OPTION) {
                
                PrintWriter pw = new PrintWriter(chooser.getSelectedFile() + ".txt");
                pw.print(accepted.toString());
                pw.close();
            }
        } catch (Exception e) {
            System.out.println("Error Occured While Exporting: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            JSONObject nv = rList.remove(jList1.getSelectedIndex());
            DefaultListModel list = new DefaultListModel();
            for(JSONObject j : rList) list.addElement(j.get(YouTubeVideo.TITLE_KEY));
            jList1.setModel(list);
            jList1.setSelectedIndex(0);
            jLabel9.setText("Accepted: " + accepted.length());
        } catch (Exception e){
            System.out.println("Error Occured While Rejecting: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

